/*23. Write a C# program to print the odd numbers from 1 to 99. 
 * Prints one number per line.*/
using System;
public class ble23
{
    public static void Main()
    {
        for (int i = 1; i < 101; i += 2)
        {
            Console.WriteLine(i);
        }
    }
}
